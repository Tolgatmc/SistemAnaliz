package com.entity;

public class Kart {
	
	private int kartid;
	
	private int spsid;
	
	private int userid;
	
	private String spsad;
	
	private String spstür;
	
	private Double fiyat;
	
	private Double total_fiyat;

	public int getKartid() {
		return kartid;
	}

	public void setKartid(int kartid) {
		this.kartid = kartid;
	}

	public int getSpsid() {
		return spsid;
	}

	public void setSpsid(int spsid) {
		this.spsid = spsid;
	}

	public int getUserid() {
		return userid;
	}

	public void setUserid(int userid) {
		this.userid = userid;
	}

	public String getSpsad() {
		return spsad;
	}

	public void setSpsad(String spsad) {
		this.spsad = spsad;
	}

	public String getSpstür() {
		return spstür;
	}

	public void setSpstür(String spstür) {
		this.spstür = spstür;
	}

	public Double getFiyat() {
		return fiyat;
	}

	public void setFiyat(Double fiyat) {
		this.fiyat = fiyat;
	}

	public Double getTotal_fiyat() {
		return total_fiyat;
	}

	public void setTotal_fiyat(Double total_fiyat) {
		this.total_fiyat = total_fiyat;
	}
	
	

}
